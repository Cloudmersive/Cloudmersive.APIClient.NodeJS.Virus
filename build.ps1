Remove-Item –path ./client –recurse

Invoke-WebRequest -Uri 'https://api.cloudmersive.com/virus/docs/v1/swagger' -OutFile '.\virus-api-swagger.json'
(Get-Content .\virus-api-swagger.json).replace('localhost', "api.cloudmersive.com") | Set-Content .\virus-api-swagger.json
(Get-Content .\virus-api-swagger.json -Raw) -replace '"http"','"https"' | Set-Content .\virus-api-swagger.json -Encoding UTF8

& java -jar swagger-codegen-cli-2.4.46.jar generate -i .\virus-api-swagger.json -l javascript -o client -c packageconfig.json
(Get-Content ./client/package.json).replace('"superagent": "3.7.0"', '"superagent": "10.2.3"') | Set-Content ./client/package.json
(Get-Content ./client/README.md).replace('This SDK is automatically generated by the [Swagger Codegen](https://github.com/swagger-api/swagger-codegen) project:', '[Cloudmersive Virus Scan API](https://www.cloudmersive.com/virus-api) provides advanced virus scanning capabilities.') | Set-Content ./client/README.md
(Get-Content ./client/README.md).replace('- Build package: io.swagger.codegen.languages.JavascriptClientCodegen', '') | Set-Content ./client/README.md
#& npm build ./client
& npm i ./client

Copy-Item ./client/README.md ./README.md